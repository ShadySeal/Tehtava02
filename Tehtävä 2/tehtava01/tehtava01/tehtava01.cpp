#include <iostream>
using namespace std;

int main() {
    cout << "Size of char: " <<
        sizeof(char) << endl;
    cout << "Size of int: " <<
        sizeof(int) << endl;
    cout << "Size of float: " <<
        sizeof(float) << endl;
    cout << "size of double: " <<
        sizeof(double) << endl;
    cout << "Size of unsigned int: " <<
        sizeof(unsigned int) << endl;
    cout << "Size of bool: " <<
        sizeof(bool) << endl;
}